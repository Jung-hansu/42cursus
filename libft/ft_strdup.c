#include "libft.h"

char	*ft_strdup(const char *str)
{
	int		i;
	int		len;
	char	*new;

	i = 0;
	len = 0;
	while (str[len])
	{
		len++;
	}
	new = (char *)malloc(sizeof(char) * (len + 1));
	if (!(new))
		return (NULL);
	while (str[i])
	{
		new[i] = str[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
